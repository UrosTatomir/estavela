                      <p class="font-italic text-left" style="font-size: 14px; color: #FED502;"> It primarily works with a shapefile format, or files are stored in that format. Shapefile is a set of at least three files with SHP, SXH and DBF extensions, and is currently the most used vector format in GIS developed due to interoperability by ESRI. Shapefile consists of geometric shapes (dot, line, or polygon), and corresponding tables. This table contains attributes of geometric shapes.</p>

                      <p class="font-italic text-left" style="font-size: 14px; color: #FED502;">Of course, Qgis allows you to view other vector formats. In addition, it also provides an overview of various raster formats.</p>

