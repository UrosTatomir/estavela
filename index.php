                      <p class="font-italic text-left" style="font-size: 14px; color: #FED502;">Vector layers contain objects that are of the same type ie. points, or lines, or polygons, and corresponding attributes. These layers can be edited (add / modify / delete objects or operations that can be performed depending on the type of objects the layer contains, ie whether objects, lines, or polygons are objects).</p>
